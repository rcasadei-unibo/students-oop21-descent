package com.unibo.util;

/**
 * Enum to differentiate a character direction.
 */
public enum Direction {
    /**
     * Right direction.
     */
    RIGHT,
    /**
     * Left direction.
     */
    LEFT,
    /**
     * Up direction.
     */
    UP,
    /**
     * Down direction.
     */
    DOWN,
    /**
     * No direction.
     */
    STILL
}
